class Notice {

    void notice(title = "", text = "") {
        script {
            echo """
>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>> ${title} <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
${text}
>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>> ${title} <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
"""
        }
    }

    def noticeWarning(text = "") {
        script {
            echo """
>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>> Warning <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
${text}
>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>> Warning <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
"""
        }
    }
}
